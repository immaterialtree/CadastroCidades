    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        
        Usuario user = new Usuario();
        
        user.nome = txtNome.getText();
        user.senha = String.valueOf(txtSenha.getPassword());
        user.email = txtEmail.getText().toLowerCase();
        user.tipo = groupTipo.getSelection() == radioComum.getModel() ? 2 :
                (groupTipo.getSelection() == radioAdmin.getModel() ? 1 : 0);
        if (user.tipo == 0) {
            JOptionPane.showMessageDialog(this, "Selecione um tipo de usuário", "Erro", JOptionPane.WARNING_MESSAGE);
            return;
        }         
        
        listUsuario = controlUser.recarregarLista();
        JOptionPane.showMessageDialog(this, "Usuario salvo\nId: "+listUsuario.get(listUsuario.size()-1).id+"\nNome: "+user.nome+"\nemail: "+user.email+"\nTipo: "+user.tipo);
        
        indice = listUsuario.size()-1;
        mostrarDadosTela(indice);
        preencherTabela();
        tabUsuarios.changeSelection(indice, 1, false, false);
        atualizarBotoes();
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        
        Usuario user = new Usuario();
        user.id = Integer.parseInt(txtId.getText());
        user.nome = txtNome.getText();
        user.senha = String.valueOf(txtSenha.getPassword());
        user.email = txtEmail.getText().toLowerCase();
        user.tipo = groupTipo.getSelection() == radioComum.getModel() ? 2 :
                (groupTipo.getSelection() == radioAdmin.getModel() ? 1 : 0);
        
        controlUser.atualizarUsuario(user);
        
        listUsuario = controlUser.recarregarLista();
        preencherTabela();
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        int id = Integer.parseInt(txtId.getText());
        controlUser.deletarUsuario(id);
        
        indice += indice>0 ? -1 : (listUsuario.isEmpty() ? -1 : +0);
        listUsuario = controlUser.recarregarLista();
        mostrarDadosTela(indice);
        preencherTabela();
        tabUsuarios.changeSelection(indice, 1, false, false);
        atualizarBotoes();
        
    }//GEN-LAST:event_btnRemoverActionPerformed

