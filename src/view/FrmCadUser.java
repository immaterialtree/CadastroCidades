    private void mostrarDadosTela(int i) {
        if (listUsuario.isEmpty()) {
            txtId.setText("");
            txtNome.setText("");
            txtSenha.setText("");
            txtEmail.setText("");
            groupTipo.clearSelection();
        } 
        else {
            txtId.setText(""+listUsuario.get(i).id);
            txtNome.setText(listUsuario.get(i).nome);
            txtSenha.setText(listUsuario.get(i).senha);
            txtEmail.setText(listUsuario.get(i).email);
            var tipo = listUsuario.get(i).tipo == 1 ? radioAdmin.getModel() : radioComum.getModel();
            groupTipo.setSelected(tipo, true);
        }
    }
    
    private void atualizarBotoes() {
        btnPrevious.setEnabled(indice>0);
        btnFirst.setEnabled(indice>0);

        btnNext.setEnabled(indice<listUsuario.size()-1);
        btnLast.setEnabled(indice<listUsuario.size()-1);
        
        btnRemover.setEnabled(indice!=-1);
        btnEditar.setEnabled(indice!=-1);
    }
    
    private void preencherTabela() {
        
        DefaultTableCellRenderer cellRender = new DefaultTableCellRenderer();
	cellRender.setHorizontalAlignment(SwingConstants.CENTER);
        tabUsuarios.getColumnModel().getColumn(0).setPreferredWidth(10);  //Preferencia de largura medidad em pixels
        tabUsuarios.getColumnModel().getColumn(1).setPreferredWidth(80);  
        tabUsuarios.getColumnModel().getColumn(2).setPreferredWidth(50);  
        tabUsuarios.getColumnModel().getColumn(3).setPreferredWidth(100);
        tabUsuarios.getColumnModel().getColumn(4).setPreferredWidth(10);
        for (int i=0; i<tabUsuarios.getColumnCount(); i++)
            tabUsuarios.getColumnModel().getColumn(i).setCellRenderer(cellRender);
        
        DefaultTableModel modelo = (DefaultTableModel) tabUsuarios.getModel();
        modelo.setNumRows(0);
        for (Usuario user: listUsuario) {
            modelo.addRow(new Object[]{
                user.id, 
                user.nome, 
                user.senha, 
                user.email,
                user.tipo});
        }
    }
    
